//lets look at some datatypes

//Strings: can be single quote, double, or back tick
console.log('I love single quotes!')
console.log("I love double quotes!")
console.log(`I love back tics!`)

//Booleans
console.log(true)
console.log(false)
console.log(2>3) //expressions

//Numbers, ints and floats
console.log(-3)
console.log(0)
console.log(3)

console.log(-3.5)
console.log(0.0)
console.log(3.0)


//Undefined vars
let undefinedVar
console.log(undefinedVar) // undefined, because it is not assigned to a value yet


//Null vars
let emptyValue = null
console.log(emptyValue)


//We can also check data types!
console.log(typeof 'String') // string
console.log(typeof 5) // number
console.log(typeof true) // boolean
console.log(typeof null) // object type
console.log(typeof undefined) // undefined